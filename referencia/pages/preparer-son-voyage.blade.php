    			<h3 id="sante-securite">Santé</h3>
    			<h4>Généralités:</h4>
    			<p>Au Pérou, il est important de respecter quelques règles d’hygiène et de santé. Nous vous conseillons donc de:</p>
    			<ul>
    				<li>Ne pas boire l’eau du robinet, sauf si elle est bouillie ou purifiée à l’aide de pastilles</li>
    				<li>Vous laver régulièrement les mains pour limiter les risques de contaminations</li>
    				<li>Éviter la viande et le poisson achetés sur les marchés : préférez les restaurants pour goûter aux spécialités telles que le ceviche</li>
    			</ul>

    			<h4>Vaccins</h4>
    			<p>Pour entrer au Pérou depuis la France, aucun vaccin n’est obligatoire. Certains sont cependant recommandés notamment les suivants:</p>
    			<ul>
    				<li>Diphtérie-tétanos-poliomyélite</li>
    				<li>Fièvre jaune (en cas de séjour en Amazonie, et qui est obligatoire en cas de séjour  au Vénézuela depuis el Pérou)</li>
    				<li>Grippe (recommandée pour les sujets les plus fragiles, de nombreux cas de grippe AH1N1 ayant été détectés au Pérou)</li>
    			</ul>

    			<h4>Séjour en Amazonie:</h4>
    			<p>En plus d’une vaccination contre la fièvre jaune, un traitement contre le paludisme peut être recommandé (consulter l’avis de votre médecin)</p>

    			<h4>Altitude:</h4>
    			<p>Un voyage au Pérou implique d’effectuer des visites et pratiquer des activités en haute altitude. Pour cette raison, nous recommandons aux personnes connaissant des problèmes cardiaques de consulter l’avis d’un médecin avant tout déplacement.</p>
    			<p>Pour les voyageurs souhaitant effectuer une randonnée ou trekking sur place, il est recommandé de s’entrainer physiquement (marche, randonnée, natation …)</p>

    			<h5>Nos conseils pour favoriser une acclimatation en douceur:</h5>
    			<ul>
    				<li>Vous hydrater correctement, boire régulièrement</li>
    				<li>Manger léger et équilibré</li>
    				<li>Vous revitaliser durant la journée (barre de céréales, fruits secs, sucre...)</li>
    			</ul>

    			<h5>Quels médicaments prendre:</h5>
    			<p>Nous recommandons de consulter l’avis de votre médecin, qui vous fournira les médicaments adéquats selon votre profil.</p>
    			<p>En cas de maux de têtes, préférez l’aspirine au paracétamol</p>
    			<p>Nous déconseillons la prise de somnifères, aggravant l’hypoxie (manque d’oxygène, cause principale du mal de l’altitude)</p>

    			<h5>Quand prendre le Diamox®:</h5>
    			<p>Le Diamox (Acétazolamide) est un diurétique qui augmente la réponse ventilatoire au manque d’oxygène. Il est donc très efficace face au mal de l’altitude.</p>
    			<p>Il est à prendre qu’en cas de maux persistants. En effet, en prenant en compte les effets indésirables du Diamox,  une acclimatation naturelle est à privilégier.</p>

    			<h5>Les désavantages du diamox:</h5>
    			<ul>
    				<li>Il peut  provoquer des effets indésirables</li>
    				<li>Le Diamox est un diurétique, et provoque donc une déshydratation</li>
    				<li>Administré préventivement il rend difficile la détection d’un problème lié à l’altitude</li>
    			</ul>

