    			<h4>Le climat:</h4>
    			<p>Ayant une grande diversité géographique, le Pérou est sujet à des climats extrêmement variés déterminés par l’altitude et les saisons. Voici une présentation de ceux-ci par région :</p>
    			<ul>
    				<li>La côte (Ica, Chiclayo, Lima, Trujillo et Tumbes) : climat désertique ou tropical aride, le soleil y est très présent de Novembre à Mai. Les températures s’adoucissent le reste de l’année, et le ciel est souvent couvert du à l’influence du courant marin « El Niño ».</li>
    				<li>Région septentrionale : climat chaud durant toute l’année avec des pics de chaleur atteignant les 35°C en été.</li>
    				<li>Région centrale et méridionale : deux saisons bien marquées : l’hiver (entre avril et octobre) et l’été (entre novembre et mars).</li>
    			</ul>

    			<p>La Sierra (Ancash, Arequipa, Ayacucho, Cajamarca, Cusco et Puno): </p>
    			<p>Cette région compte deux saisons : l’hiver, entre avril et octobre ; et l’été de novembre à mars. L’hiver est une saison sèche, tandis que l’été est pluvieux.</p>
    			<p>La Selva ou forêt tropicale (Iquitos, Madre de Dios et Manu) :</p>
    			<p>La Selva alta (forêt d’altitude : environ 700 mètres d’altitude) possède un climat subtropical caractérisé par de fortes pluies. La forêt basse (en dessous de 700 mètres d’altitude) présente quant à elle deux saisons : l’hiver ou saison sèche (d’avril à octobre) avec un climat ensoleillé et chaud. Pendant l’été ou saison des pluies (de novembre à mars), les averses sont plus fréquentes.</p>

