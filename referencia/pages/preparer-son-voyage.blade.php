    			<h3 id="milleures">Meilleures périodes pour venir</h3>
    			<p>Le Pérou est aussi constitué de régions aussi nombreuses que distinctes (côte, Andes, Amazonie) caractérisése par des climats très différents les uns des autres: </p>
    			<ul>
    				<li><strong>Sur la côte</strong>, l’été austral (novembre à avril) affiche de fortes températures, tandis que l’hiver est plus frais (mais jamais froid) et marqué par des brumes matinales.</li>
    				<li><strong>Dans les Andes et en Amazonie</strong>, il existe deux saisons principales : la saison sèche (mai à octobre) et la saison humide (novembre à avril). S’il est tout à fait possible de voyager dans la région andine durant la saison des pluies, il est cependant fort déconseillé de randonner en montagne, où les chutes de neige rendant les sentiers dangereux.  </li>
    				<li><strong>Dans les Andes</strong>, le temps peut être très changeant au cours d’une même journée. Nous vous conseillons donc de prévoir des vêtements variés, c'est-à-dire chauds pour les soirées fraîches, plus légers pour les journées souvent ensoleillés, et un vêtement de pluie.</li>
    			</ul>

    			<p>Le Pérou se trouvant très proche de l’Equateur, le soleil y est vertical et fort. Ce phénomène est largement amplifié par l’altitude. N’oubliez donc pas de vous munir d’un chapeau, de crème solaire et de lunettes de soleil à fort indice de protection.</p>

