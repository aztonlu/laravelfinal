    			<h3 id="fetes-jours">Les fêtes et jours fériés au Pérou</h3>
    			<p>Au Pérou, la vie quotidienne est ponctuée d’un grand nombre de fêtes qui ont lieu tout au long de l’année, jusque dans les villages les plus isolés du pays où saints et croyances sont célébrés en grande pompe.</p>

    			<p>Voici quelques-unes des festivités les plus importantes, auxquelles vous pourrez peut-être assister selon votre date de voyage: </p>

    			<h5><strong>2 février : la Virgen de la Candelaria (Vierge de la Chandeleur)</strong></h5><br>
    			<p>Elle fut nommée ainsi suite à la vision qu’eut un père jésuite de la Vierge Marie luttant contre un démon. Cette fête dure deux semaines, le jour principal étant le 2 février. Assimilée à une forme de carnaval, elle est caractérisée par de nombreux défilés et concours de danses folkloriques. Le 10 et le 11 février sont deux jours également importants puisque se déroulent dans le stade et dans les rues de Puno les concours de danse opposant les différents quartiers de la ville. Les costumes les plus fous et les plus colorés illuminent alors la capitale de l’Altiplano qui ne vit plus que pour la fête jusqu’au 15 février. </p>

    			<h5><strong>La Semaine sainte</strong></h5>
    			<p>Elle correspond à la dernière semaine du Carême. Les festivités commencent véritablement le jeudi suivant le dimanche des Rameaux et se prolonge jusqu’au dimanche de Pâques.  Des processions ont alors lieu dans tout le pays, avec pour personnages principaux les icônes religieuses telles que celle de la Vierge Marie. Les processions les plus impressionnantes et réputées ont lieu dans les villes d’Ayacucho, de Huaraz et de Cusco. Durant ces festivités sont associées d’anciennes croyances ancestrales aux traditions catholiques importées d’Europe.</p>

