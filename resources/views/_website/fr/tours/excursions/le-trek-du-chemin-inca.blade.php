                <p>Randonner sur le chemin de l’Inca, c’est la façon la plus originale de se rendre a Machu Picchu. Vous effectuerez un véritable voyage à travers le temps sur ce chemin qu’empruntaient les Incas pour se rendre de Cusco, leur capitale, à la citadelle haut perchée. Vous passerez au pied des cimes enneigées, dans un paysage aride, avant de descendre vers la forêt tropicale et sa végétation luxuriante. En 4 jours de marche, vous rallierez ainsi deux milieux naturels très différents. Votre effort sera récompensé par une approche tout à fait originale du site de Machu Picchu, que vous découvrirez dans les brumes et le silence du petit matin depuis la Porte du Soleil.</p>
