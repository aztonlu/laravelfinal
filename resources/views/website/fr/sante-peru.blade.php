                        <h2>Santé / Sécurité</h2>
                        <p align="justify">Généralités : Au Pérou, il est important de respecter quelques règles d’hygiène et de santé. Nous vous conseillons donc de :<br>
 
                        • Ne pas boire l’eau du robinet, sauf si elle est bouillie ou purifiée à l’aide de pastilles. <br>
                        • Vous laver régulièrement les mains pour limiter les risques de contaminations. <br>
                        • Éviter la viande et le poisson achetés sur les marchés : préférez les restaurants pour goûter aux spécialités telles que le ceviche.  <br><br>

                        Vaccins : Pour entrer au Pérou depuis la France, aucun vaccin n’est obligatoire. Certains sont cependant recommandés notamment les suivants :<br>
                        • Diphtérie-tétanos-poliomyélite<br>
                        • Fièvre jaune (en cas de séjour en Amazonie, et qui est obligatoire en cas de séjour  au Vénézuela depuis el Pérou) <br>
                        • Grippe (recommandée pour les sujets les plus fragiles, de nombreux cas de grippe AH1N1 ayant été détectés au Pérou)<br><br>
 
                        Séjour en Amazonie : En plus d’une vaccination contre la fièvre jaune, un traitement contre le paludisme peut être recommandé (consulter l’avis de votre médecin)  <br>
                        Altitude : Un voyage au Pérou implique d’effectuer des visites et pratiquer des activités en haute altitude. Pour cette raison, nous recommandons aux personnes connaissant des problèmes cardiaques de consulter l’avis d’un médecin avant tout déplacement. Pour les voyageurs souhaitant effectuer une randonnée ou trekking sur place, il est recommandé de s’entrainer physiquement (marche, randonnée, natation …)<br><br>
 
                        Nos conseils pour favoriser une acclimatation en douceur :<br>
 
                        • Vous hydrater correctement, boire régulièrement <br>
                        • Manger léger et équilibré <br>
                        • Vous revitaliser durant la journée (barre de céréales, fruits secs, sucre..)<br><br>
 
                        Quels médicaments prendre :<br>
 
                        Nous recommandons de consulter l’avis de votre médecin, qui vous fournira les médicaments adéquats selon votre profil.<br>
 
                        En cas de maux de têtes, préférez l’aspirine au paracétamol <br>
 
                        Nous déconseillons la prise de somnifères, aggravant l’hypoxie (manque d’oxygène, cause principale du mal de l’altitude).<br><br>
 
                        Quand prendre le Diamox® : <br>
 
                        Le Diamox (Acétazolamide) est un diurétique qui augmente la réponse ventilatoire au manque d’oxygène. Il est donc très efficace face au mal de l’altitude. <br>
 
                        Il est à prendre qu’en cas de maux persistants. En effet, en prenant en compte les effets indésirables du Diamox,  une acclimatation naturelle est à privilégier. <br>
 
                        Les désavantages du diamox : <br>
 
                        • Il peut  provoquer des effets indésirables  <br>
                        • le Diamox est un diurétique, et provoque donc une déshydratation <br>
                        • Administré préventivement il rend difficile la détection d’un problème lié à l’altitude <br><br></p>


                    <p align="justify">
                        En séjour organisé, il existe peu de risques à voyager au Pérou. Cependant, il est préférable de suivre certaines règles lors de vos déplacements en transport en commun ou de vos visites libres en ville : <br>
 
                        • Conservez une copie de votre passeport séparément de votre passeport. Ainsi, en cas de vol, il sera plus facile d’éditer  des papiers temporaires.   <br>
                        • Pour éviter d’attirer l’attention sur votre argent, gardez-le sur vous, dans vos poches ou dans une sacoche près du corps.  <br>
                        • Dans les transports en commun, conservez toujours vos objets de valeur sur vos genoux, à portée de regard, afin de vous assurer que personne n’y fouille.   <br>
                        • Ne vous aventurez pas seul dans des zones rurales reculées sans avoir de contact local. Les risques d’agression sont minimes mais il est préférable, dans tous les cas, d’être accompagné afin de pouvoir échanger plus facilement avec les locaux. 
