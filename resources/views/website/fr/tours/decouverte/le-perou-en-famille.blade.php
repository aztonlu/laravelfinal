                            <h4 class="text-success">JOUR 9: PUNO (3820 M) - CUSCO (3350 m)</h4>
                            <p>Départ matinal pour Cusco. Vous traversez le plateau du Colla où vous vous arrêtez au village de Pukara. C’est l’un des derniers endroits témoignant de la culture pré-inca du même nom.</p>
                            <p>Vous passez ensuite le col de La Raya (4360 m), symbolisant la fin de l’Altiplano et le début de la région de Cusco. C’est à Sicuani que vous faites l’expérience d’un déjeuner typique avant de reprendre la route en direction de Raqchi. Vous y visitez le spectaculaire sanctuaire inca, construit par Pachacutec en honneur au dieu inca Wiracocha. Il possède une grande muraille centrale dont la base, taillée en pierre, témoigne de la finesse de l’architecture inca, tandis qu’une autre partie des remparts a été construite en blocs d\'adobe. De plus, la structure possède deux colonnes latérales circulaires. Autour du temple se trouvent divers bâtiments qui avaient autrefois fonction d’habitation et d’entrepôts.</p>
