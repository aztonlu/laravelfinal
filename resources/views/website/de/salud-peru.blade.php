                        • Halten Sie eine Kopie Ihres Passes getrennt von Ihrem Reisepass. So, im Falle von Diebstahl, wird es einfacher, temporäre Papiere zu veröffentlichen.   <br>
                        • Um zu vermeiden, die Aufmerksamkeit auf Ihr Geld zu ziehen, halten Sie es mit Ihnen, in Ihrer Tasche oder in einer Tasche in der Nähe Ihres Körpers.  <br>
