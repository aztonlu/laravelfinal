                        • Diphtherie, Tetanus und Poliomyelitis<br>
                        • Gelbfieber (bei Aufenthalt im Amazonas, obligatorisch bei Aufenthalt in Venezuela aus Peru) <br>
                        • Influenza (empfohlen für die meisten zerbrechlichen Themen, viele Fälle von Influenza AH1N1 in Peru entdeckt)<br><br>
 
                        Bleiben Sie im Amazonasgebiet: Zusätzlich zu einem Gelbfieberimpfstoff kann eine Malariabehandlung empfohlen werden (wenden Sie sich an den Rat Ihres Arztes) <br>
                        Höhe: Eine Reise nach Peru beinhaltet Besuche und Aktivitäten in Höhenlagen. Aus diesem Grund empfehlen wir, dass Menschen mit Herzproblemen einen Ratgeber beraten, bevor sie reisen. Für Reisende, die wandern oder wandern wollen, ist es empfehlenswert, körperlich zu trainieren (zu Fuß, Joggen, Schwimmen ...)<br><br>
 
                        Unser Rat zur Förderung einer sanften Akklimatisierung :<br>
 
                        • Es hydratisiert richtig, trinkt regelmäßig <br>
                        • Essen Sie Licht und ausgeglichen <br>
                        • Es wird während des Tages revitalisiert (Bar von Getreide, Nüsse, Zucker ..)<br><br>
 
                        Welche Medikamente nehmen:<br>
 
                        Wir empfehlen Ihnen, den Rat Ihres Arztes zu konsultieren, der Ihnen die richtigen Medikamente nach Ihrem Profil zur Verfügung stellt.<br>
 
                        Im Falle von Kopfschmerzen, Aspirin zu Acetaminophen <br>
 
                        Es wird empfohlen, keine Schlaftabletten zu nehmen, verschlimmernde Hypoxie (Sauerstoffmangel, die Hauptursache der Höhenkrankheit).<br><br>
 
                        Cuándo tomar Diamox®: <br>
 
                        Diamox (Acetazolamid) ist ein Diuretikum, das die Beatmungsreaktion auf Sauerstoffmangel erhöht. Daher ist es sehr effektiv gegen Höhenkrankheit. <br>
 
                        Es sollte nur im Falle von anhaltenden Beschwerden genommen werden. In der Tat, angesichts der unerwünschten Wirkungen von Diamox, ist natürliche Akklimatisierung bevorzugt. <br>
 
                        Die Nachteile von diamox: <br>
 
                        • Kann Nebenwirkungen verursachen <br>
                        • Diamox ist ein Diuretikum und verursacht daher Dehydratation <br>
                        • Vorbeugende Verabreichung macht es schwierig, ein Höhenproblem zu erkennen<br><br></p>


