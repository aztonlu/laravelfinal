                        <p align="justify">Allgemein: In Peru ist es wichtig, einige Regeln der Hygiene und Gesundheit zu beachten. Wir empfehlen:<br>
 
                        • Trinken Sie kein Leitungswasser, es sei denn, es wird gekocht oder mit Tabletten gereinigt. <br>
                        • Waschen Sie regelmäßig Ihre Hände, um das Risiko einer Kontamination zu reduzieren. <br>
                        • Vermeiden Sie das Fleisch und die Fische, die auf den Märkten gekauft werden: Sie bevorzugen die Restaurants, um Spezialitäten wie die Ceviche zu probieren.  <br><br>

