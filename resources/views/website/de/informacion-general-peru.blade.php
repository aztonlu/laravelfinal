                        <p align="justify">Peru hat   <br>
                        • Eine Bevölkerung von etwa 30 Millionen. <br>
                        • Ein BIP von $ 301,5 Milliarden im Jahr 2011. <br>
                        • Eine Gesamtfläche von 1.285.220 km², dreimal die Fläche von Frankreich. <br>
                        • 2414 km Küstenlinie. <br>
                        • Ein Land mit einer gemeinsamen Grenze mit Chile, Argentinien, Brasilien, Ecuador und Bolivien. <br>
                        • Ein Land mit dem Gipfel des schneebedeckten Huascarán, auf 6768 Meter über dem Meeresspiegel.    <br>
                        <br>
                        Ein besonderes Ziel:  <br>
                        • Peru hat 28 der weltweit 31 Klimate (in einigen Teilen des Landes ist das Klima das ganze Jahr über sonnig) <br>
                        • 84 der weltweit 104 Ökosysteme sind in Peru präsent. <br>
                        • Das Land hat die zweithöchste Bergkette der Welt. <br>
                        • Der Colca Canyon ist der zweitgrößte Canyon der Welt (doppelt so tief wie der Grand Canyon). <br>
                        • Das Land hat acht Nationalparks und neun Welterbestätten.     <br>
                        <br>
                        Klima: Mit einer großen geographischen Vielfalt unterliegt Peru extrem unterschiedlichen Klimazonen, bestimmt durch die Höhe und die Jahreszeiten. Hier ist eine Präsentation von diesen nach Region:   <br>
                        • Die Küste (Ica, Chiclayo, Lima, Trujillo und Tumbes): Trockenes oder Wüstenklima, die Sonne ist von November bis Mai sehr präsent. Die Temperaturen erweichen den Rest des Jahres, und der Himmel wird oft durch den Einfluss des Sees "El Niño" abgedeckt.   <br>
                        • Nordregion: Warmes Klima während des ganzen Jahres mit Hitze, die im Sommer 35 ° C erreicht. <br>
                        • Mittel- und Südregionen: zwei gut markierte Jahreszeiten: Winter (zwischen April und Oktober) und Sommer (zwischen November und März).   <br>
                        Die Sierra (Ancash, Arequipa, Ayacucho, Cajamarca, Cusco und Puno): Diese Region hat zwei Jahreszeiten: Winter, zwischen April und Oktober; Und Sommer von November bis März. Winter ist eine trockene Jahreszeit, während der Sommer regnerisch ist.  <br>

                        Der Dschungel oder der Regenwald (Iquitos, Madre de Dios und Manu): La Selva alta (Höhenwald: ca. 700 Meter über dem Meeresspiegel) hat ein subtropisches Klima mit starken Regenfällen. Der Tiefwald (unter 700 Meter) präsentiert zwei Jahreszeiten: den Winter oder die Trockenzeit (von April bis Oktober) mit einem sonnigen und warmen Klima. Während des Sommers oder der Regenzeit (von November bis März) sind Duschen häufiger.<br>
                        <br>
                        Die Fauna und Flora: Peru ist ein Land, das reich an Biodiversität ist. Es hat derzeit mehr als 40.000 Arten von Pflanzen, darunter 3.000 Arten von Orchideen. Peruanische Flüsse haben 2000 Fischarten, mehr als der Atlantik. <br>
                        Mehr als 3700 Arten von Schmetterlingen können im Land beobachtet werden, was einen Weltrekord darstellt. Peru ist die Heimat von 1710 Vogelarten, von denen 300 endemisch sind. Die Welt der Säugetiere besteht aus 32 Arten von Affen, Jaguaren, rosa Süßwassendelphinen (Schweinswalen) und Riesenotter im Regenwald; Die Anden-Fauna besteht aus Pumas, Lamas, Alpakas und Vicuñas, ohne den Kondor zu vergessen, der größte Vogel der Welt mit 3,6 Metern Spannweite.   <br>
                        <br>
                        Gastronomie: Peru ist in ganz Südamerika bekannt für die Vielfalt seiner Gastronomie, aus einer Mischung aus spanischer und einheimischer Küche. Gourmet Reisende haben die Möglichkeit, Spezialitäten wie zu probieren: <br>  
                        • Ceviche, nationales Gericht: leicht gekocht oder roher Fisch, mariniert in Zitronensaft! <br>  
                        • Pisco Sour: Ein leicht süßes und funkelndes Getränk aus Pisco (weißer Traubenkohol), Limettensaft, Eiweiß und Crushed Ice. Sehr erfrischend! <br>  
                        • Coca-mate: eine Infusion von Coca-Blatt (Andenpflanze), die die Verdauung hilft und ein wirksames Heilmittel für Höhenprobleme ist. <br> • Alpaka-Steak: alles Geschmack eines Steaks, aber kein Cholesterin!  <br> 
                        • Lomo saltado: Fleisch mit Gemüse und Pommes frites, mit Reis serviert <br>
                        • Das Meerschweinchen, häufiger genannt cuy, Kochen im Ofen mit Gemüse. Sein Fleisch ist sehr zart und duftend.  <br> 
                        • Die Anticuchos: Rindern auf dem Grill oder Rindfleisch.  <br>
                         Und vergessen Sie nicht die vielen Sorten von tropischen Früchten und Gemüse, von wo aus Sie köstliche frische Fruchtsäfte probieren können, die in allen lokalen Märkten genossen werden können. </p>
