                        <h2>Urlaub und Ferien in Peru</h2>
                        <p align="justify">In Peru ist das tägliche Leben von einer großen Anzahl von Feierlichkeiten geprägt, die während des ganzen Jahres stattfinden, auch in den abgelegensten Dörfern des Landes, wo Heiligen und Glauben mit großem Pomp gefeiert werden.  <br><br>


                        Hier sind einige der wichtigsten Festlichkeiten, die Sie je nach Reisezeit teilnehmen können: <br>

                        2. Februar: Virgen de la Candelaria (Virgen de la Candelaria) Es wurde nach der Vision eines Jesuitenpriesters der Jungfrau Maria benannt, der gegen einen Dämon kämpft. Dieses Festival dauert zwei Wochen und ist der Haupttag am 2. Februar. Als eine Form des Karnevals ist es durch zahlreiche Paraden und Wettbewerbe des Folklore-Tanzes gekennzeichnet. 10. und 11. Februar sind zwei wichtige Tage, da die Tanzwettbewerbe zwischen den verschiedenen Stadtteilen der Stadt im Stadion und in den Straßen von Puno stattfinden. Die wildesten und buntesten Kostüme erhellen die Hauptstadt des Altiplano, die bis zum 15. Februar nur für das Festival lebt. <br><br>

                        Karwoche: Das ist die letzte Woche der Fastenzeit. Die Feierlichkeiten beginnen am Donnerstag nach Palmsonntag und fahren bis Ostersonntag. <br>
                        Die Prozessionen wurden im ganzen Land durchgeführt, mit Hauptfiguren religiöse Ikonen wie die Jungfrau Maria. <br>
                        Die beeindruckendsten und berühmtesten Prozessionen finden in den Städten Ayacucho, Huaraz und Cusco statt. Während dieser Feierlichkeiten sind alte uralte Überzeugungen mit katholischen Traditionen aus Europa importiert verbunden. <br>
                        Q'eswachaca-Brücke: Jedes Jahr treffen sich die Gemeinden Huinchiri, Chaupibanda, Ccollana Quehue und Pelcaro, um die letzte Inka-Brücke in Peru wiederherzustellen. <br>
                        In effet, ce pont entièrement réalisé von cordes faites d'herbe tressée nécessite une restauration annuelle.<br>
                        Dies ist die Gelegenheit für 4 Tage, um diese Veranstaltung zusammen zu feiern. Ein einzigartiger und unvergesslicher Moment in unserem Unternehmen zu leben! <br><br>

                        31. Mai 2015: Q'oyllur Riti Fest Es ist eines der größten indigenen Festivals in Lateinamerika und bringt jährlich mehr als achtzigtausend Pilger zusammen. "Ccoyllor Ritti" ist ein Quechua Wort Bedeutung 'Snow Star'; Dieser Anden-Glaube wurde von der katholischen Religion verwandelt und angepasst, um durch Taytacha Qoylloriti (Lord of the Snows) ersetzt zu werden. <br>
                        Die Bewohner des Gebietes von Ocongate (2 Stunden von Cusco) führen einen jährlichen Ritus durch, dessen Symbol die Ikone Christi ist, die die Integration des Menschen mit der Natur bedeutet. Dieses Fest ist mit der Fruchtbarkeit der Erde verbunden, aber auch mit den Geistern von Tieren und Bergen (Apus) und anderen Schutzgöttern. Das Hauptritual findet am Fuße des Berges von Sinakara statt, wo lange Prozessionen von Pilgern aus der ganzen Region kommen.
                        <br><br>
                        4. Juni 2015: Corpus Christi Vor der Einführung des Katholizismus in Peru gab es ein gebürtiges Fest, das die 14 Inka-Könige und Kaiser in Cusco, der Hauptstadt des Reiches, feierte. In der Tat, die Bewohner paradierten die Mumien ihrer verstorbenen Könige durch die Prozessionen durch die Stadt. Aber überrascht von dieser Übung verwandelten und ersetzten die Spanier die Mumien mit 14 reich gekleideten katholischen Ikonen. <br>So jedes Jahr seit Beginn der Kolonialzeit, nimmt jede Gemeinde von Cusco und seine Umgebung seinen Schutzpatron in der Prozession zur Kathedrale, um es gesegnet zu haben.  <br><br>
                        24. Juni: Das Inti Raymi Dieses Fest ist eine Erholung des Festes der Sonne, die im Inka-Reich gefeiert wurde und feierte die Wintersonnenwende, den kürzesten Tag des Jahres, das Zeichen des Ende eines Bauernjahres und der Anfang von Ein anderer Die Sonne wurde verehrt, um ihn mit zahlreichen Tieropfern und Opfern zu ehren, um seine Rückkehr und damit die schöne Jahreszeit zu sichern. Viele der Inkentänze und Bräuche werden durch eine großartige Show mit Tausenden von Schauspielern den ganzen Tag wieder aufgebaut. <br>Die Repräsentation findet am Ort der Festung von Sacsayhuaman statt und verleiht ihm gleichzeitig eine historische und mystische Dimension.  
                        <br><br>
                        16. Juli: La Virgen del Carmen Dieses Festival wird direkt von der spanisch-katholischen Tradition geerbt. Es bezieht sich auf eine Erscheinung der Jungfrau Maria auf dem Karmel in Israel. <br>Eines der Zentren der Verehrung der Virgen del Carmen in Peru ist Paucartambo. Paraden von Tänzern mit bunten Kostümen finden in der ganzen Region von Cusco statt und an diesem Tag die Jungfrau segnet die Teilnehmer und beseitigt die Dämonen.  <br><br>

                        28. Juli: Nationaler Tag von Peru Feiert die Unabhängigkeitserklärung von Peru aus Jose San Martin, der am 28. Juli 1821 Lima nahm. Bei dieser Gelegenheit werden Militär- und Studentenparaden im ganzen Land abgehalten. 15. August: Jungfrau Assunta Wie in allen katholischen Ländern ist die Annahme ein wichtiges Fest. <br>
                        So sind am 15. August die großen Städte das Theater der Prozessionen und Festlichkeiten.  <br><br>

                        14. September: Pilgerfahrt des Herrn von Huanca Diese Pilgerfahrt ist eine jährliche Sitzung in Erinnerung an die Inkas, die von den Spaniern während der Kolonialzeit besetzt wurden. Der Herr von Huanca wurde die Ikone der armen und schlecht behandelten Arbeiter. Am 14. September gingen die Einwohner von Cusco nach Huanca, einer Stadt in der Nähe von Pisaq, wo sich das Heiligtum des Herrn von Huanca befindet.</p>
