                        <h2>Salud / Seguridad</h2>
                        <p align="justify">General: En Perú, es importante respetar algunas reglas de higiene y salud. Le aconsejamos que:<br>
 
                        • No beba agua del grifo a menos que sea hervida o purificada usando pastillas. <br>
                        • Lávese las manos regularmente para reducir el riesgo de contaminación. <br>
                        • Evitar la carne y el pescado comprados en los mercados: prefieren los restaurantes para probar especialidades como el ceviche.  <br><br>

                        Vacunas: Para ingresar al Perú desde Francia, ninguna vacuna es obligatoria. Sin embargo, se recomiendan algunos de los siguientes: :<br>
                        • La difteria, el tétanos y la poliomielitis<br>
                        • Fiebre amarilla (en caso de estancia en el Amazonas, obligatoria en caso de estancia en Venezuela desde el Perú) <br>
                        • Influenza (recomendado para los sujetos más frágiles, muchos casos de influenza AH1N1 detectados en el Perú)<br><br>
 
                        Estancia en la Amazonia: Además de una vacuna contra la fiebre amarilla, se puede recomendar un tratamiento de la malaria (consulte el consejo de su médico)  <br>
                        Altitud: Un viaje al Perú implica visitas y actividades en altitudes elevadas. Por esta razón, recomendamos que las personas con problemas cardiacos consulten el consejo de un médico antes de viajar. Para los viajeros que desean caminar o trek en el lugar, se recomienda entrenar físicamente (caminar, trotar, nadar ...)<br><br>
 
                        Nuestro consejo para promover una aclimatación suave :<br>
 
                        • Se hidrata correctamente, bebe con regularidad <br>
                        • Coma ligero y equilibrado <br>
                        • Se revitaliza durante el día (barra de cereales, frutos secos, azúcar ..)<br><br>
 
                        Qué medicamentos tomar:<br>
 
                        Le recomendamos que consulte el consejo de su médico, quien le proporcionará los medicamentos adecuados según su perfil.<br>
 
                        En caso de dolores de cabeza, obtenga aspirina al paracetamol <br>
 
                        Se aconseja no tomar pastillas para dormir, agravando la hipoxia (falta de oxígeno, principal causa de mal de altura).<br><br>
 
                        Cuándo tomar Diamox®: <br>
 
                        Diamox (Acetazolamide) es un diurético que aumenta la respuesta ventilatoria a la falta de oxígeno. Por lo tanto, es muy eficaz contra el mal de la altitud. <br>
 
                        Se debe tomar sólo en caso de dolencias persistentes. De hecho, teniendo en cuenta los efectos indeseables de Diamox, se prefiere una aclimatación natural. <br>
 
                        Las desventajas de diamox : <br>
 
                        • Puede causar efectos secundarios  <br>
                        • Diamox es un diurético, y por lo tanto causa la deshidratación <br>
                        • Administrado preventivamente hace que sea difícil detectar un problema relacionado con la altitud<br><br></p>


                    <p align="justify">
                        Al permanecer organizado, hay pocos riesgos para viajar en Perú. Sin embargo, es mejor seguir ciertas reglas cuando viaja por transporte público o sus visitas gratuitas a la ciudad : <br>
 
                        • Guarde una copia de su pasaporte por separado del pasaporte. Así, en caso de robo, será más fácil publicar papeles temporales.   <br>
                        • Para evitar llamar la atención a su dinero, guárdelo con usted, en su bolsillo o en una bolsa cerca de su cuerpo.  <br>
                        • En transporte público, siempre tenga objetos de valor en su regazo, a la vista, para asegurarse de que nadie está observando.   <br>
                        • No aventurarse solo en las zonas rurales remotas sin contacto local. Los riesgos de agresión son mínimos, pero en todos los casos es preferible estar acompañados para poder intercambiar la comunicación. 
