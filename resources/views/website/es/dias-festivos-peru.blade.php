                        <h2>Vacaciones y feriados en Perú</h2>
                        <p align="justify">En el Perú, la vida cotidiana está marcada por un gran número de fiestas que se realizan a lo largo del año, incluso en los pueblos más remotos del país, donde se celebran con gran pompa los santos y las creencias.  <br><br>


                        Aquí están algunas de las festividades más importantes, que usted puede poder atender dependiendo de su fecha del recorrido: <br>

                        2 de febrero: Virgen de la Candelaria (Virgen de la Candelaria) Fue nombrada por la visión de un sacerdote jesuita de la Virgen María luchando contra un demonio. Este festival dura dos semanas, siendo el día principal el 2 de febrero. Como una forma de carnaval, se caracteriza por numerosos desfiles y concursos de danza folklórica. Los días 10 y 11 de febrero son dos días importantes ya que las competiciones de baile entre los distintos barrios de la ciudad tienen lugar en el estadio y en las calles de Puno. Los trajes más salvajes y más coloridos iluminan la capital del Altiplano, que vive sólo para el festival hasta el 15 de febrero. <br><br>

                        Semana Santa: Esta es la última semana de Cuaresma. Las fiestas empiezan realmente el jueves siguiente al Domingo de Ramos y continúan hasta el Domingo de Pascua.  <br>
                        Las procesiones se realizaron en todo el país, con personajes principales iconos religiosos como el de la Virgen María. <br>
                        Las procesiones más impresionantes y famosas tienen lugar en las ciudades de Ayacucho, Huaraz y Cusco. Durante estas festividades se asocian antiguas creencias ancestrales con tradiciones católicas importadas de Europa.  <br>
                        Puente Q'eswachaca: Cada año, las comunidades de Huinchiri, Chaupibanda, Ccollana Quehue y Pelcaro se reúnen para restaurar el último puente Inca en Perú. <br>
                        En effet, ce pont entièrement réalisé à partir de cordes faites d’herbe tressée nécessite une restauration annuelle. <br>
                        Esta es la oportunidad por 4 días para celebrar juntos este evento. Un momento único e inolvidable para vivir en nuestra empresa!  <br><br>

                        31 de mayo de 2015: Fiesta de Q'oyllur Riti Es una de las mayores fiestas indígenas de América Latina, reuniendo a más de ochenta mil peregrinos cada año. "Ccoyllor Ritti" es una palabra quechua que significa 'Estrella de las Nieves'; Esta creencia andina fue transformada y adaptada por la religión católica, para ser reemplazada por Taytacha Qoylloriti (Señor de las Nieves). <br>
                        Los habitantes de la zona de Ocongate (a 2h30 de Cusco) realizan un rito anual, cuyo símbolo es el icono de Cristo que significa la integración del hombre con la naturaleza. Esta fiesta se asocia con la fertilidad de la tierra, pero también con los espíritus de animales y montañas (apus) y otros dioses tutelares. El ritual principal tiene lugar a los pies de la montaña de Sinakara, donde acuden largas procesiones de peregrinos de toda la región. 
                        <br><br>
                        4 de junio de 2015: Corpus Christi Antes de la introducción del catolicismo en el Perú, hubo un festival nativo que celebraba a los 14 reyes y emperadores incas en Cusco, la capital del imperio. De hecho, los habitantes hicieron desfilar a las momias de sus difuntos reyes a través de las procesiones por la ciudad. Pero sorprendidos por esta práctica, los españoles la transformaron y reemplazaron a las momias con 14 iconos católicos ricamente vestidos. <br>Así, cada año desde el comienzo del período colonial, cada parroquia de Cusco y sus alrededores lleva a su santo patrón en procesión a la catedral para tenerla bendita.  <br><br>
                        24 de junio: El Inti Raymi Esta fiesta es una recreación de la fiesta del Sol que se celebró dentro del Imperio Inca y celebró el solsticio de invierno, el día más corto del año, la señal del fin de Un año agrícola y el comienzo de otro. El sol era venerado honrándolo con numerosos sacrificios de animales y ofrendas para asegurar su regreso y por tanto el de la hermosa temporada. Muchas de las danzas y costumbres incas se reconstruyen a través de un gran espectáculo con miles de actores a lo largo del día. <br>La representación tiene lugar en el sitio de la fortaleza de Sacsayhuaman, confiriéndole al mismo tiempo una dimensión histórica y mística.  
                        <br><br>
                        16 de julio: La Virgen del Carmen Este festival es heredado directamente de la tradición católica española. Se refiere a una aparición de la Virgen María en el Monte Carmelo en Israel. <br>Uno de los centros del culto de la Virgen del Carmen en Perú es Paucartambo. Desfiles de bailarines con vistosos disfraces tienen lugar en toda la región de Cusco y ese día la Virgen bendice a los participantes y elimina los demonios.  <br><br>

                        28 de julio: Día Nacional del Perú Celebra la declaración de independencia del Perú de José San Martín que tomó Lima el 28 de julio de 1821. En esta ocasión, desfiles militares y de estudiantes se llevan a cabo en Todo el país. 15 de agosto: Virgen Asunta Como en todos los países católicos, la Asunción es una fiesta importante. <br>
                        Así, el 15 de agosto, las grandes ciudades son el teatro de las procesiones y festividades.  <br><br>

                        14 de septiembre: Peregrinación del Señor de Huanca Esta peregrinación es un encuentro anual que se celebra en memoria de los incas minados por los españoles durante la época colonial. El Señor de Huanca se convirtió en el icono de los trabajadores pobres y maltratados. El 14 de septiembre, los habitantes del Cusco se dirigieron a Huanca, un pueblo cercano a Pisaq, donde se encuentra el santuario del Señor de Huanca.</p>
