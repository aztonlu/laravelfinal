                        <h2>Informaci&oacute;n de caracter general</h2>
                        <p align="justify">Per&oacute; tiene:   <br>
                        • Una población de unos 30 millones. <br>
                        • Un PBI de $ 301.500 millones en 2011. <br>
                        • Una superficie total de 1.285.220 km², tres veces la superficie de Francia. <br>
                        • 2414 Km de costa. <br>
                        • Un país con frontera común con Chile, Argentina, Brasil, Ecuador y Bolivia. <br>
                        • Un país con la cumbre del nevado Huascarán, a 6768 metros sobre el nivel del mar.     <br>
                        <br>
                        Un destino especial:   <br>
                        • Perú tiene 28 de los 31 climas del planeta (en algunas partes del país, el clima es soleado todo el año) <br>
                        • 84 de los 104 ecosistemas del mundo están presentes en Perú. <br>
                        • El país tiene la segunda cordillera más alta del mundo. <br>
                        • El Cañón del Colca es el segundo cañón más profundo del mundo (dos veces más profundo que el Gran Cañón). <br>
                        • El país cuenta con ocho parques nacionales y nueve sitios del Patrimonio Mundial.     <br>
                        <br>
                        Clima: Con una gran diversidad geográfica, Perú está sujeto a climas extremadamente variados, determinados por la altitud y las estaciones. He aquí una presentación de estos por región:   <br>
                        • La costa (Ica, Chiclayo, Lima, Trujillo y Tumbes): clima árido o desértico, el sol está muy presente de noviembre a mayo. Las temperaturas se ablandan el resto del año, y el cielo es cubierto a menudo por la influencia de la corriente marina de "El Niño".   <br>
                        • Región Norte: clima cálido durante todo el año con picos de calor que alcanzan los 35 ° C en verano. <br>
                        • Regiones centrales y meridionales: dos estaciones bien marcadas: invierno (entre abril y octubre) y verano (entre noviembre y marzo).   <br>
                        La Sierra (Ancash, Arequipa, Ayacucho, Cajamarca, Cusco y Puno): Esta región tiene dos estaciones: invierno, entre abril y octubre; Y verano de noviembre a marzo. El invierno es una estación seca, mientras que el verano es lluvioso.   <br>

                        La Selva o Selva Tropical (Iquitos, Madre de Dios y Manu): La Selva alta (bosque de altitud: unos 700 metros de altitud) tiene un clima subtropical caracterizado por fuertes lluvias. El bosque bajo (por debajo de 700 metros) presenta dos estaciones: el invierno o la estación seca (de abril a octubre) con un clima soleado y cálido. Durante el verano o la temporada de lluvias (de noviembre a marzo), las duchas son más frecuentes. <br>
                        <br>
                        La fauna y la flora: Perú es un país inmensamente rico en biodiversidad. Actualmente cuenta con más de 40.000 especies de plantas, incluyendo 3.000 especies de orquídeas. Los ríos peruanos tienen 2000 especies de peces, más que el Océano Atlántico. <br>
                        Más de 3700 especies de mariposas se pueden observar en el país, lo que representa un récord mundial. Perú es el hogar de 1710 especies de aves de la lista, 300 de los cuales son endémicos. El mundo de los mamíferos se compone de 32 especies de monos, jaguares, delfines de agua dulce de color rosa (botos) y nutrias gigantes en la selva tropical; La fauna andina está compuesta por pumas, llamas, alpacas y vicuñas, sin olvidar el cóndor, el ave más grande del mundo con sus 3.6 metros de envergadura.    <br>
                        <br>
                        Gastronomía: El Perú es conocido en toda América del Sur por la diversidad de su gastronomía, hecha de una mezcla de cocina española y nativa. Los viajeros gourmet tendrán la oportunidad de probar especialidades como: <br>  
                        • Ceviche, plato nacional: pescado ligeramente cocinado o crudo, marinado en jugo de limón! <br>  
                        • El Pisco Sour: una bebida ligeramente dulce y chispeante hecha de Pisco (alcohol de uva blanca), jugo de lima, clara de huevo y hielo triturado. Muy refrescante! <br>  
                        • Coca-mate: una infusión de hoja de coca (planta andina) que ayuda a la digestión y es un remedio eficaz para los problemas de altitud. <br> • Filete de alpaca: todo el sabor de un bistec pero sin colesterol!  <br> 
                        • El lomo saltado: carne en rodajas con verduras y papas fritas, servido con arroz  <br>
                        • El cobayo, más comúnmente llamado cuy, que cocina en el horno con verduras. Su carne es muy tierna y fragante.  <br> 
                        • Los anticuchos: brochetas de carne de res a la parrilla, o carne de res.  <br>
                         Y no se olvide de las muchas variedades de frutas y hortalizas tropicales, desde donde se pueden degustar deliciosos zumos de frutas frescas que se pueden disfrutar en todos los mercados locales. </p>
