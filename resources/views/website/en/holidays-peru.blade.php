                        <h2>Holidays in Peru</h2>
                        <p align="justify">In Peru, daily life is marked by a large number of celebrations that take place throughout the year, even in the most remote villages of the country, where saints and beliefs are celebrated with great pomp.  <br><br>


                        Here are some of the most important festivities, which you may be able to attend depending on your travel date: <br>

                        February 2: Virgen de la Candelaria (Virgen de la Candelaria) It was named after the vision of a Jesuit priest of the Virgin Mary fighting against a demon. This festival lasts two weeks, being the main day on February 2nd. As a form of carnival, it is characterized by numerous parades and contests of folkloric dance. February 10 and 11 are two important days as the dance competitions between the different neighborhoods of the city take place in the stadium and in the streets of Puno. The wildest and most colorful costumes illuminate the capital of the Altiplano, which lives only for the festival until February 15. <br><br>

                        Holy Week: This is the last week of Lent. The festivities really begin the Thursday after Palm Sunday and continue until Easter Sunday.  <br>
                        The processions were carried out throughout the country, with main characters religious icons such as the Virgin Mary. <br>
                        The most impressive and famous processions take place in the cities of Ayacucho, Huaraz and Cusco. During these festivities are associated ancient ancestral beliefs with Catholic traditions imported from Europe.  <br>
                        Q'eswachaca bridge: Each year, the communities of Huinchiri, Chaupibanda, Ccollana Quehue and Pelcaro meet to restore the last Inca bridge in Peru. <br>
                        In effet, ce pont entièrement réalisé from cordes faites d'herbe tressée nécessite une restauration annuelle. <br>
                        This is the opportunity for 4 days to celebrate this event together. A unique and unforgettable moment to live in our company! <br><br>

                        May 31, 2015: Q'oyllur Riti Feast It is one of the largest indigenous festivals in Latin America, bringing together more than eighty thousand pilgrims each year. "Ccoyllor Ritti" is a Quechua word meaning 'Star of the Snow'; This Andean belief was transformed and adapted by the Catholic religion, to be replaced by Taytacha Qoylloriti (Lord of the Snows). <br>
                        The inhabitants of the area of Ocongate (2 hours away from Cusco) perform an annual rite, whose symbol is the icon of Christ that means the integration of man with nature. This feast is associated with the fertility of the earth, but also with the spirits of animals and mountains (apus) and other tutelary gods. The main ritual takes place at the foot of the mountain of Sinakara, where long processions of pilgrims from all over the region come.
                        <br><br>
                        June 4, 2015: Corpus Christi Before the introduction of Catholicism in Peru, there was a native festival that celebrated the 14 Inca kings and emperors in Cusco, the capital of the empire. In fact, the inhabitants paraded the mummies of their dead kings through the processions through the city. But surprised by this practice, the Spaniards transformed and replaced the mummies with 14 richly dressed Catholic icons. <br>Thus every year since the beginning of the colonial period, each parish of Cusco and its surrounding area takes its patron saint in procession to the cathedral to have it blessed.  <br><br>
                        June 24: The Inti Raymi This feast is a recreation of the Feast of the Sun which was celebrated within the Inca Empire and celebrated the winter solstice, the shortest day of the year, the sign of the end of a farming year and the beginning of other. The sun was revered honoring him with numerous animal sacrifices and offerings to ensure his return and therefore the beautiful season. Many of the Inca dances and customs are rebuilt through a great show with thousands of actors throughout the day. <br> The representation takes place in the site of the fortress of Sacsayhuaman, conferring at the same time a historical and mystical dimension.

                        <br><br>
                        1July 6: La Virgen del Carmen This festival is inherited directly from the Spanish Catholic tradition. It refers to an apparition of the Virgin Mary on Mount Carmel in Israel. <br> One of the centers of worship of the Virgen del Carmen in Peru is Paucartambo. Parades of dancers with colorful costumes take place throughout the region of Cusco and that day the Virgin blesses the participants and eliminates the demons.  <br><br>

                        July 28: National Day of Peru Celebrates the declaration of independence of Peru from Jose San Martin that took Lima on July 28, 1821. On this occasion, military and student parades are held in the whole country. August 15: Virgin Assunta As in all Catholic countries, the Assumption is an important feast. <br>
                         Thus, on August 15, the great cities are the theater of processions and festivities.  <br><br>

                        September 14: Pilgrimage of the Lord of Huanca This pilgrimage is an annual meeting held in memory of the Incas mined by the Spanish during the colonial era. The Lord of Huanca became the icon of the poor and ill-treated workers. On September 14, the inhabitants of Cusco went to Huanca, a town near Pisaq, where the sanctuary of the Lord of Huanca is located.</p>
