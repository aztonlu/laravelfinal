                        In case of headaches, get aspirin to acetaminophen <br>
                        It is advised not to take sleeping pills, aggravating hypoxia (lack of oxygen, the main cause of altitude sickness).<br><br>
 
                        When to take Diamox®: <br>
                        Diamox (Acetazolamide) is a diuretic that increases the ventilatory response to lack of oxygen. Therefore, it is very effective against altitude sickness. <br>
 
                        It should be taken only in case of persistent ailments. In fact, given the undesirable effects of Diamox, natural acclimatization is preferred. <br>
 
                         The disadvantages of diamox: <br>
 
                        • May cause side effects <br>
                         • Diamox is a diuretic, and therefore causes dehydration<br>
                        • Preventively administered makes it difficult to detect an altitude related problem<br><br></p>


