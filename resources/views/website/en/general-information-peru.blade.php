                        Climate: With a great geographical diversity, Peru is subject to extremely varied climates, determined by the altitude and the seasons. Here is a presentation of these by region:   <br>
                        • The coast (Ica, Chiclayo, Lima, Trujillo and Tumbes): arid or desert climate, the sun is very present from November to May. Temperatures soften the rest of the year, and the sky is often covered by the influence of the "El Niño" sea current.   <br>
                        • North Region: warm climate throughout the year with peaks of heat reaching 35 ° C in summer. <br>
                        • Central and southern regions: two well-marked seasons: winter (between April and October) and summer (between November and March).   <br>
                        The Sierra (Ancash, Arequipa, Ayacucho, Cajamarca, Cusco and Puno): This region has two seasons: winter, between April and October; And summer from November to March. Winter is a dry season, while summer is rainy.  <br>

                        The Jungle or Rainforest (Iquitos, Madre de Dios and Manu): La Selva alta (high altitude forest: about 700 meters above sea level) has a subtropical climate characterized by heavy rains. The low forest (below 700 meters) presents two seasons: the winter or the dry season (from April to October) with a sunny and warm climate. During the summer or the rainy season (from November to March), showers are more frequent. <br>
                        <br>
                        The fauna and flora: Peru is a country immensely rich in biodiversity. It currently has more than 40,000 species of plants, including 3,000 species of orchids. Peruvian rivers have 2000 species of fish, more than the Atlantic Ocean.<br>
                        More than 3700 species of butterflies can be observed in the country, representing a world record. Peru is home to 1710 bird species listed, 300 of which are endemic. The world of mammals consists of 32 species of monkeys, jaguars, pink freshwater dolphins (porpoises) and giant otters in the rainforest; The Andean fauna is composed of pumas, llamas, alpacas and vicuñas, not to mention the condor, the largest bird in the world with its 3.6 meters of wingspan.    <br>
                        <br>
                        Gastronomy: Peru is known throughout South America for the diversity of its gastronomy, made of a mixture of Spanish and native cuisine. Gourmet travelers will have the opportunity to try specialties such as: <br>  
                        • Ceviche, national dish: lightly cooked or raw fish, marinated in lemon juice! <br>  
                        • Pisco Sour: A slightly sweet and sparkling drink made from Pisco (white grape alcohol), lime juice, egg white and crushed ice. Very refreshing! <br>  
                        • Coca-mate: an infusion of coca leaf (Andean plant) that helps digestion and is an effective remedy for altitude problems. <br> • Alpaca steak: all the flavor of a steak but no cholesterol!  <br> 
                        • Lomo saltado: sliced meat with vegetables and French fries, served with rice <br>
                        • The guinea pig, more commonly called cuy, cooking in the oven with vegetables. Its meat is very tender and fragrant.  <br> 
                        • The anticuchos: brochettes of beef on the grill, or beef.  <br>
