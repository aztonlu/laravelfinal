                        <h2>Vacanze in Perù</h2>
                        <p align="justify">In Perù, la vita quotidiana è caratterizzata da un gran numero di celebrazioni che si svolgono tutto l'anno, anche nei villaggi più lontani del paese, dove i santi e le credenze si celebrano con grande pompa. <br><br>


                        Ecco alcune delle feste più importanti, che potreste essere in grado di partecipare a seconda della data di viaggio: <br>

                        2 febbraio: Virgen de la Candelaria (Virgen de la Candelaria) È stato chiamato dopo la visione di un sacerdote gesuita della Vergine Maria che combatte contro un demone. Questo festival dura due settimane, essendo il giorno principale del 2 febbraio. Come una forma di carnevale, è caratterizzata da numerose sfilate e concorsi di danza folklorica. Il 10 e il 11 febbraio sono due giorni importanti, mentre le gare di danza tra i diversi quartieri della città si svolgono nello stadio e nelle vie di Puno. I costumi più selvaggi e più colorati illuminano la capitale dell'Altiplano, che vive solo per il festival fino al 15 febbraio. <br>

                        Settimana Santa: Questa è l'ultima settimana della Quaresima. Le festività iniziano davvero il giovedì dopo la domenica delle Palme e proseguono fino alla domenica di Pasqua. <br>

                        Le processioni sono state eseguite in tutto il paese, con personaggi principali icone religiose come la Vergine Maria. <br>

                        Le processioni più impressionanti e famose si svolgono nelle città di Ayacucho, Huaraz e Cusco. Durante queste feste sono associate antiche credenze ancestrali con tradizioni cattoliche importate dall'Europa. <br>

                        Inca ponte di corda: Ogni anno, le comunità Huinchiri, Chaupibanda, Ccollana Quehue e Pelcaro incontrano per ripristinare l'ultimo ponte Inca in Perù. <br>

                        In effetti, ce pont entièrement réalisé da corde faites d'herbe tressée nécessite une restauration annuelle. <br>

                        Questa è l'occasione per 4 giorni per celebrare insieme questo evento. Un momento unico e indimenticabile per vivere nella nostra azienda! <br><br>

                        31 maggio 2015: Festa del Q'oyllur Riti è uno dei più grandi partiti indigeni in America Latina, che riunisce più di ottanta mila pellegrini ogni anno. "Ccoyllor Ritti" è una parola Quechua che significa "Stella della neve"; Questa credenza andina è stata trasformata e adattata dalla religione cattolica, da sostituire da Taytacha Qoylloriti (Signore delle Nevi). <br>

                        Gli abitanti dell'area di Ocongate (2 ore di distanza da Cusco) eseguono un rito annuale, il cui simbolo è l'icona di Cristo che significa l'integrazione dell'uomo con la natura. Questa festa è associata alla fertilità della terra, ma anche agli spiriti degli animali e delle montagne (apus) e di altri dè tutelari. Il rituale principale si svolge ai piedi della montagna di Sinakara, dove arrivano lunghe processioni di pellegrini provenienti da tutta la regione.
                        <br>

                        4 Giugno 2015: Corpus Christi Prima dell'introduzione del cattolicesimo in Perù, era una festa che celebra i 14 nativo re e imperatori Incas a Cusco, la capitale dell'impero. Infatti, gli abitanti hanno sfilato le mummie dei loro re di morte attraverso le processioni attraverso la città. Ma sorpreso da questa pratica, gli spagnoli trasformarono e sostituirono le mummie con 14 icone cattoliche riccamente vestite. <br> Così ogni anno dall'inizio del periodo coloniale, ogni parrocchia di Cusco e dei suoi dintorni conduce alla loro santo patrono in processione alla cattedrale di averlo benedetto. <br>


                        24 giugno: L'Inti Raymi Questo festival è una ricreazione della festa del Sole tenuto all'interno della Imperio Inca e ha celebrato il solstizio d'inverno, il giorno più corto dell'anno, il segno della fine di un anno agricolo e l'inizio del altro. Il sole fu venerato per onorarlo con numerosi sacrifici animali e offerte per assicurare il suo ritorno e quindi la bella stagione. Molte delle danze e delle abitudini Inca sono ricostruite attraverso un grande spettacolo con migliaia di attori durante il giorno. <br> La rappresentazione si svolge nel sito della fortezza di Sacsayhuaman, conferendo allo stesso tempo una dimensione storica e mistica.
                        <br>

                        16 luglio: La Virgen del Carmen Questo festival è ereditato direttamente dalla tradizione cattolica spagnola. Si riferisce ad un'apparizione della Vergine Maria sul Monte Carmelo in Israele. <br> Uno dei centri di culto della Virgen del Carmen in Perù è Paucartambo. Sfilate di ballerini con costumi colorati si svolgono in tutta la regione di Cusco e quel giorno la Vergine benedica i partecipanti ed elimina i demoni. <br>

                        28 luglio: Festa nazionale del Perù si compiace della dichiarazione di indipendenza del Perù Jose San Martin che ha preso Lima il 28 luglio 1821. In questa occasione, parate militari e studenti si svolgono in tutto il paese. 15 agosto: Virgin Asunta Come in tutti i paesi cattolici, dell'Assunta è una festa importante. <br>
                        Così, il 15 agosto, le grandi città sono teatro di processioni e feste. <br>

                        14 settembre: Pellegrinaggio del Signore degli Huanca Questo pellegrinaggio è un evento annuale che si tiene in memoria degli Incas minati dagli spagnoli durante il periodo coloniale. Lord of Huanca è diventato l'icona dei lavoratori poveri e maltrattati. Il 14 settembre, gli abitanti di Cusco è andato a Huanca, un villaggio vicino a Pisac, dove il santuario del Signore di Huanca è.</p>
