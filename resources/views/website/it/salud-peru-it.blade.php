                        • Idrata correttamente, bevande regolarmente
                        • Mangia leggera e bilanciata
                        • Rivitalizza durante il giorno (barra di cereali, noci, zucchero ..) <br> <br>
 
                        Quali farmaci da prendere: <br>

                        Si consiglia di consultare il consiglio del medico, che vi fornirà i farmaci giusti in base al tuo profilo.
 
                        In caso di mal di testa, aspirina per acetaminophen <br>

                        Si consiglia di non prendere pillole per dormire, aggravando l'ipossia (mancanza di ossigeno, principale causa di malattia di altitudine).
 
                        Quando prendere Diamox®: <br>

                        Diamox (Acetazolamide) è un diuretico che aumenta la risposta respiratoria alla mancanza di ossigeno. Quindi è molto efficace contro la malattia di altitudine. <br>


                        Dovrebbe essere assunto solo in caso di disturbi persistenti. Infatti, dato gli effetti indesiderati di Diamox, è preferibile l'acclimatazione naturale. <br>
 
                        Gli svantaggi di diamox: <br>
 
                        • Può causare effetti collaterali
                        • Diamox è un diuretico e causa quindi disidratazione
                        • Gestione preventiva rende difficile rilevare un problema legato all'altitudine<br><br></p>


