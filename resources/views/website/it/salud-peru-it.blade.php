                        Altitudine: un viaggio in Perù comporta visite e attività a quota elevata. Per questo motivo, consigliamo che le persone con problemi cardiaci consultino un consiglio medico prima di viaggiare. Per i viaggiatori che vogliono camminare o trekking sul sito, si consiglia di allenarsi fisicamente (passeggiate, jogging, nuoto ...) <br> <br>
 
