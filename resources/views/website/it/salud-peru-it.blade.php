                        • Febbre gialla (in caso di soggiorno nell'Amazzonia, obbligatoria in caso di soggiorno in Venezuela dal Perù)
                        • Influenza (raccomandata per i soggetti più fragili, molti casi di influenza AH1N1 rilevati in Perù) <br> <br>


